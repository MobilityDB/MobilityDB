/**
 * @brief A simple program that reads from a CSV file synthetic trip data in
 * Brussels generated by the MobilityDB-BerlinMOD generator,
 * https://github.com/MobilityDB/MobilityDB-BerlinMOD
 * and performs a temporal count aggregation.
 *
 * Please read the assumptions made about the input file `trips.csv` in the
 * file `05_meos_disassemble_berlinmod.c` in the same directory.
 *
 * The program can be build as follows
 * @code
 * gcc -Wall -g -I/usr/local/include -o 09_meos_aggregate_berlinmod 09_meos_aggregate_berlinmod.c -L/usr/local/lib -lmeos
 * @endcode
 */

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <meos.h>

typedef struct
{
  int tripid;
  int vehid;
  DateADT day;
  int seq;
  Temporal *trip;
} trip_record;

trip_record trip_rec;

/* Maximum length in characters of a trip in the input data */
#define MAX_LENGTH_TRIP 170001
/* Maximum length in characters of a header in the input CSV file */
#define MAX_LENGTH_HEADER 1024
/* Maximum length in characters of a date in the input data */
#define MAX_LENGTH_DATE 12

/* Main program */
int main(void)
{
  /* Variables to read the input CSV file */
  char header_buffer[MAX_LENGTH_HEADER];
  char date_buffer[MAX_LENGTH_DATE];
  char trip_buffer[MAX_LENGTH_TRIP];

  /* Initialize MEOS */
  meos_initialize(NULL);

  /* You may substitute the full file path in the first argument of fopen */
  FILE *file = fopen("trips.csv", "r");

  if (! file)
  {
    printf("Error opening file\n");
    return 1;
  }

  int no_records = 0, i;

  /* Read the first line of the file with the headers */
  fscanf(file, "%1023s\n", header_buffer);

  /* Variable keeping the current aggregate state */
  SkipList *state = NULL;
  STBox *extent = NULL;
  Interval *interval = pg_interval_in("1 hour", -1);
  TimestampTz origin = pg_timestamptz_in("2020-06-01", -1);

  /* Continue reading the file */
  do
  {
    int read = fscanf(file, "%d,%d,%10[^,],%d,%170000[^\n]\n",
      &trip_rec.tripid, &trip_rec.vehid, date_buffer, &trip_rec.seq, trip_buffer);
    /* Transform the string representing the date into a date value */
    DateADT day = pg_date_in(date_buffer);
    trip_rec.day = day;
    /* Transform the string representing the trip into a temporal value */
    trip_rec.trip = temporal_from_hexwkb(trip_buffer);

    if (read == 5)
      no_records++;

    if (read != 5 && ! feof(file))
    {
      printf("Trip record with missing values\n");
      fclose(file);
      /* Free memory */
      free(trip_rec.trip);
      return 1;
    }

    if (ferror(file))
    {
      printf("Error reading file\n");
      fclose(file);
      /* Free memory */
      free(trip_rec.trip);
      return 1;
    }

    /* Add the current value to the running aggregates */
    extent = tpoint_extent_transfn(extent, trip_rec.trip);
    /* Get the time of the trip at an hour granularity */
    SpanSet *ps = periodset_tprecision(temporal_time(trip_rec.trip), interval, origin);
    /* Aggregate the time of the trip */
    state = periodset_tcount_transfn(state, ps);
    /* Free memory */
    free(trip_rec.trip);

  } while (!feof(file));

  printf("\n%d trip records read\n\n", no_records);

  printf("Extent\n");
  printf("------\n\n");
  printf("\%s\n\n", stbox_out(extent, 6));

  printf("Temporal count\n");
  printf("--------------\n\n");
  int seqcount;
  Temporal *tcount = temporal_tagg_finalfn(state);
  Temporal **tcount_seqs = (Temporal **) temporal_sequences(tcount, &seqcount);
  for (i = 0; i < seqcount; i++)
    printf("\%s\n", tint_out(tcount_seqs[i]));

  /* Free memory */
  free(tcount_seqs);

  /* Close the input file */
  fclose(file);

  /* Finalize MEOS */
  meos_finalize();

  return 0;
}
