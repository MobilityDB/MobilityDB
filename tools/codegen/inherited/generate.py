#!/usr/bin/env python3
"""Inherited-behaviour SQL generator for Temporal<T> subclasses."""
# Emits the SQL surface that every TNumber<T> / TSpatial<T> subtype INHERITS via
# late binding + lifting (compops, topops, posops, spatialrels, ...). These files
# are pure wiring to the generic Tspatial_* / Temporal_* wrappers and are identical
# across families modulo the type tokens, so they are generated from one template
# per behaviour rather than hand-maintained per family (see memory
# temporal-oo-late-binding-architecture + meos-api-codegen-regularity).
#
#   python tools/codegen/inherited/generate.py            # emit new (non-reference) types
#   python tools/codegen/inherited/generate.py --check     # dry-run, list target paths
#   python tools/codegen/inherited/generate.py --validate   # self-regen reference == committed
from __future__ import annotations
import argparse
import pathlib
import sys

try:
    import yaml  # pyyaml
except ImportError:
    sys.exit("pyyaml is required: pip install pyyaml")

HERE = pathlib.Path(__file__).resolve().parent
ROOT = HERE.parents[2]
MANIFEST = HERE / "manifest.yaml"
TEMPLATES = HERE / "templates"

BANNER = (
    "/* GENERATED by tools/codegen/inherited/generate.py from "
    "templates/{tmpl} — DO NOT EDIT BY HAND;\n"
    " * edit the template + tools/codegen/inherited/manifest.yaml and re-run. */\n")


def apply_conditionals(text: str, sub: dict) -> str:
    """Strip ``-- @IF <flag>`` / ``-- @ENDIF`` blocks whose flag is false."""
    # The flag defaults to true in the subtype. Marker lines are always removed,
    # so a subtype with the flag on reproduces the reference byte-for-byte (e.g.
    # cbuffer keeps its scalar stbox cast; a cell-index like quadbin sets
    # scalar_stbox_cast: false to drop it).
    out, skip = [], False
    for line in text.splitlines(keepends=True):
        s = line.strip()
        if s.startswith("-- @IF "):
            skip = not sub.get(s[len("-- @IF "):].strip(), True)
            continue
        if s.startswith("-- @ENDIF"):
            skip = False
            continue
        if not skip:
            out.append(line)
    return "".join(out)


def render(behaviour: str, sub: dict) -> str:
    tmpl = apply_conditionals((TEMPLATES / f"{behaviour}.sql.tmpl").read_text(), sub)
    # The cell-boundary function name defaults to `<temp>CellToBoundary`
    # (e.g. tquadbinCellToBoundary), but the h3 family - whose cell functions carry
    # the pg-h3 name temporalized by a bare `t` prefix (h3CellToBoundary ->
    # th3CellToBoundary, NOT th3indexCellToBoundary) - overrides it via `boundary_fn`.
    boundary = sub.get("boundary_fn", sub["temp"] + "CellToBoundary")
    # Capitalized base = the C-symbol stem for the scalar base::stbox cast
    # (Cbuffer_to_stbox, Pose_to_stbox, Npoint_to_stbox — all base.capitalize()).
    # Families whose C symbol does not follow that rule override it via `cbase`.
    cbase = sub.get("cbase", sub["base"].capitalize())
    body = (tmpl.replace("{TEMP}", sub["temp"])
                .replace("{BASE}", sub["base"])
                .replace("{CBASE}", cbase)
                .replace("{BRIEF}", sub["brief"])
                .replace("{BOUNDARY}", boundary))
    return BANNER.format(tmpl=f"{behaviour}.sql.tmpl") + body


# --- C boxops (box-type axis) --------------------------------------------
# The bounding-box topological operator wrappers are generated per BOX TYPE
# (stbox/tbox/tstzspan/tpcbox) into the region between these markers, which live
# inside a hand-written .c file that also holds non-generated helpers (trajectory
# tiling, NAD, ...). Only the marked region is owned by the generator.
def _boxops_markers(box: str):
    """BEGIN/END markers keyed by box type, so one file can host several
    generated regions (temporal_boxops.c holds both tstzspan and tbox)."""
    begin = (f"/* GENERATED-BOXOPS-BEGIN {box} — tools/codegen/inherited/generate.py "
             "from templates/boxops.c.tmpl; DO NOT EDIT BY HAND;\n"
             " * edit the template + manifest.yaml (boxtypes) and re-run. */\n")
    return begin, f"/* GENERATED-BOXOPS-END {box} */\n"
# The template defines one implementation per OP (overlaps/contains/contained/
# same/adjacent) as three "direction kinds", reused across every box type:
#   0 = box-first    Boxop_{BOX}_{TSIDE}   dispatch {DISPBOX}  (INVERT)
#   1 = tside-first  Boxop_{TSIDE}_{BOX}   dispatch {DISPBOX}  (INVERT_NO)
#   2 = tside-tside  Boxop_{TSIDE}_{TSIDE} dispatch {DISPTT}
# A box type is a LIST of directions. A plain box (stbox/tpcbox/tstzspan) has the
# three template directions. A composite box (tbox) whose value dimension is a
# span<T> prepends a `valspan` sub-block — its 2 box-directions (numspan_tnumber,
# tnumber_numspan) dispatch on span primitives — before its own 3 box-directions.
# This is the single generation mechanism for {tstzspan, stbox, tpcbox, tbox, …}.


def _boxops_blocks():
    """boxops.c.tmpl split on blank lines: [header], 3 dispatchers (kinds 0/1/2),
    then per op [banner, wrap0, wrap1, wrap2], [trailer]. Roundtrips exactly."""
    blocks = (TEMPLATES / "boxops.c.tmpl").read_text().split("\n\n")
    header, trailer = blocks[0], blocks[-1]
    dispatch = blocks[1:4]
    ops = [(blocks[4 + i * 4], blocks[5 + i * 4:8 + i * 4]) for i in range(5)]
    return header, dispatch, ops, trailer


def _boxops_directions(bt: dict) -> list:
    """Ordered directions for a box type: an optional value-span sub-block (kinds
    0,1 on span primitives) followed by the box's own 3 directions (kinds 0,1,2)."""
    dirs = []
    vs = bt.get("valspan")
    if vs:
        dirs += [{**vs, "kind": k} for k in (0, 1)]
    dirs += [{**bt, "kind": k} for k in (0, 1, 2)]
    return dirs


def _boxops_sub(fragment: str, d: dict, bt: dict) -> str:
    """Substitute the per-direction tokens into one template block. {PRIM} is the
    box name for a self-contained box (overlaps_stbox_stbox) but differs for a
    span<T> instance (overlaps_span_span for tstzspan/numspan) — defaults to box."""
    vals = {
        "BOX": d["box"], "BOXC": d["boxc"], "GETARG": d["getarg"],
        "PRIM": d.get("prim", d["box"]),
        "DISPBOX": d.get("dispbox", ""), "DISPTT": d.get("disptt", bt.get("disptt", "")),
        "BOXDESC": d.get("boxdesc", bt["boxdesc"]),
        "BOXDESCPL": d.get("boxdescpl", bt["boxdescpl"]),
        "TSIDE": bt["tside"], "VALDESC": bt["valdesc"], "INGROUP": bt["ingroup"],
    }
    for k, v in vals.items():
        fragment = fragment.replace("{" + k + "}", v)
    return fragment


def render_boxops(bt: dict) -> str:
    header, dispatch, ops, trailer = _boxops_blocks()
    dirs = _boxops_directions(bt)
    out = [header]
    out += [_boxops_sub(dispatch[d["kind"]], d, bt) for d in dirs]
    for banner, wraps in ops:
        out.append(banner)
        out += [_boxops_sub(wraps[d["kind"]], d, bt) for d in dirs]
    out.append(trailer)
    return "\n\n".join(out)


def splice_boxops(filetext: str, box: str, rendered: str) -> str:
    """Replace the text strictly between the box's BEGIN/END markers with `rendered`."""
    begin, end = _boxops_markers(box)
    b = filetext.index(begin) + len(begin)
    e = filetext.index(end)
    return filetext[:b] + rendered + filetext[e:]


def extract_boxops(filetext: str, box: str) -> str:
    """Return the current text between the box's BEGIN/END markers (for --validate)."""
    begin, end = _boxops_markers(box)
    b = filetext.index(begin) + len(begin)
    e = filetext.index(end)
    return filetext[b:e]


# --- C spatialrels (predicate axis) --------------------------------------
# The ever/always PG wrapper surface (E<Rel>/A<Rel>_<dir> + @sqlfn) is generated
# per FAMILY into the region between the GENERATED-SPATIALRELS markers of a
# hand-written .c file (mirroring boxops). Each predicate contributes a section
# banner + one wrapper block per (direction, quantifier); every block just
# dispatches to the hand-written MEOS ea_<rel>_<dir> kernel with EVER/ALWAYS, so
# the whole surface is 100% mechanical from (predicate, quantifier, direction) —
# which makes a missing overload or a wrong-direction/quantifier tag impossible.
def _spatialrels_markers(family: str):
    begin = (f"/* GENERATED-SPATIALRELS-BEGIN {family} — "
             "tools/codegen/inherited/generate.py from templates/spatialrels.c.tmpl;\n"
             " * DO NOT EDIT BY HAND; edit the template + manifest.yaml "
             "(spatialrel_families) and re-run. */\n")
    return begin, f"/* GENERATED-SPATIALRELS-END {family} */\n"


# The three ever/always directions in file order and the generic PG dispatcher
# each maps to. Each direction emits an EVER then ALWAYS wrapper; the @brief noun
# phrase is per-predicate (transitive "X contains Y" vs symmetric "X and Y are
# disjoint"), so it comes from the manifest `briefs` map, not a shared subj/obj.
# A predicate may restrict itself to a subset of directions (`directions:`), swap
# the dispatcher (`disp:`, e.g. dwithin's EA_dwithin_* which carries a distance),
# and pin one MEOS kernel across both arg orders (`meos_fixed:`, dwithin's
# ea_dwithin_tgeo_geo) — every other predicate uses the defaults below.
_SR_ORDER = ["geo_tgeo", "tgeo_geo", "tgeo_tgeo"]
_SR_DISP = {
    "geo_tgeo":  "EA_spatialrel_geo_tspatial",
    "tgeo_geo":  "EA_spatialrel_tspatial_geo",
    "tgeo_tgeo": "EA_spatialrel_tspatial_tspatial",
}


def _wrap_brief(text: str, width: int = 80) -> str:
    """Greedy word-wrap a @brief string to the 80-column house style. The first
    line carries the ` * @brief ` prefix, continuation lines ` * `; returns the
    text to substitute for {BRIEF} (continuation lines already prefixed)."""
    first, cont = " * @brief ", " * "
    lines, cur, prefix = [], "", first
    for w in text.split(" "):
        cand = w if not cur else cur + " " + w
        if len(prefix) + len(cand) <= width:
            cur = cand
        else:
            lines.append(cur)
            cur, prefix = w, cont
    lines.append(cur)
    return lines[0] + "".join("\n" + cont + l for l in lines[1:])


def render_spatialrels(fam: dict) -> str:
    """Render the ever/always PG wrapper region for one family: per predicate a
    banner + 6 wrapper blocks (3 directions x ever/always). Roundtrips exactly."""
    banner_t, block_t = (TEMPLATES / "spatialrels.c.tmpl").read_text().split("\n\n")
    block_t = block_t.rstrip("\n")
    out = []
    for pred in fam["predicates"]:
        out.append(banner_t.replace("{BANNER}", pred["banner"]))
        disp_over = pred.get("disp", {})
        meos_fixed = pred.get("meos_fixed")
        for direc in pred.get("directions", _SR_ORDER):
            disp = disp_over.get(direc, _SR_DISP[direc])
            meos = meos_fixed or f"ea_{pred['rel']}_{direc}"
            for ea, word, low in (("E", "ever", "e"), ("A", "always", "a")):
                brief = _wrap_brief(
                    "Return true if " + pred["briefs"][direc].replace("{word}", word))
                sub = {
                    "FN": f"{ea}{pred['rel']}_{direc}",
                    "BRIEF": brief,
                    "SQLFN": f"{low}{pred['Rel']}",
                    "DISP": disp,
                    "MEOS": meos,
                    "EA": "EVER" if ea == "E" else "ALWAYS",
                }
                frag = block_t
                for k, v in sub.items():
                    frag = frag.replace("{" + k + "}", v)
                out.append(frag)
    # Trailing newline so the region ends "}\n" before the END marker line
    # (mirrors the boxops convention where the region content ends with "\n").
    return "\n\n".join(out) + "\n"


def splice_spatialrels(filetext: str, family: str, rendered: str) -> str:
    begin, end = _spatialrels_markers(family)
    b = filetext.index(begin) + len(begin)
    e = filetext.index(end)
    return filetext[:b] + rendered + filetext[e:]


def extract_spatialrels(filetext: str, family: str) -> str:
    begin, end = _spatialrels_markers(family)
    b = filetext.index(begin) + len(begin)
    e = filetext.index(end)
    return filetext[b:e]


def target_path(behaviour: str, sub: dict, positions: dict) -> pathlib.Path:
    # The within-50-bin offset defaults to the shared `positions` map (the tight
    # cbuffer-anchored layout); a family on the tgeo-aligned layout overrides a
    # behaviour's offset via a per-subtype `positions` map (rgeo: indexes at +23).
    pos = {**positions, **sub.get("positions", {})}
    num = sub["bin"] + pos[behaviour]
    # The SQL family directory defaults to the base type name (quadbin, cbuffer),
    # but a family whose directory differs from its base type (h3: dir `h3`, base
    # `h3index`) overrides it with an explicit `fam`.
    fam = sub.get("fam", sub["base"])
    # The filename type token defaults to the temp type, but a family whose files
    # abbreviate it (rgeo: type `trgeometry`, files `..._trgeo_...`) overrides it
    # with an explicit `stem`.
    stem = sub.get("stem", sub["temp"])
    return ROOT / "mobilitydb" / "sql" / fam / f"{num}_{stem}_{behaviour}.in.sql"


def strip_banner(text: str) -> str:
    """Drop a leading GENERATED banner from a generated file."""
    # Lets the generated text be compared to a hand-written committed reference
    # (which has none).
    i = text.find("/*****")
    return text[i:] if i != -1 else text


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--check", action="store_true", help="dry-run: list target paths")
    ap.add_argument("--validate", action="store_true",
                    help="regenerate reference subtypes and diff vs committed")
    ap.add_argument("--only", help="restrict to one temp type (e.g. tquadbin)")
    args = ap.parse_args()

    mf = yaml.safe_load(MANIFEST.read_text())
    positions = mf["positions"]
    subs = mf["subtypes"]
    if args.only:
        subs = [s for s in subs if s["temp"] == args.only]

    if args.validate:
        ok = True
        for sub in mf["subtypes"]:
            if not sub.get("reference"):
                continue
            for beh in sub["files"]:
                p = target_path(beh, sub, positions)
                gen = strip_banner(render(beh, sub))
                cur = p.read_text() if p.exists() else ""
                same = gen == cur
                ok = ok and same
                print(f"[{'OK ' if same else 'DIFF'}] self-regen {sub['temp']} "
                      f"{beh} -> {p.relative_to(ROOT)}")
                if not same:
                    g, c = gen.splitlines(), cur.splitlines()
                    for n, (a, b) in enumerate(zip(g, c), 1):
                        if a != b:
                            print(f"     first diff line {n}:\n       gen: {a!r}\n"
                                  f"       cur: {b!r}")
                            break
                    if len(g) != len(c):
                        print(f"     line count gen={len(g)} cur={len(c)}")
        for bt in mf.get("boxtypes", []):
            if not bt.get("reference"):
                continue
            p = ROOT / bt["file"]
            gen = render_boxops(bt)
            cur = extract_boxops(p.read_text(), bt["box"]) if p.exists() else ""
            same = gen == cur
            ok = ok and same
            print(f"[{'OK ' if same else 'DIFF'}] self-regen boxops {bt['box']} "
                  f"-> {pathlib.Path(bt['file'])}")
            if not same:
                g, c = gen.splitlines(), cur.splitlines()
                for n, (a, b) in enumerate(zip(g, c), 1):
                    if a != b:
                        print(f"     first diff line {n}:\n       gen: {a!r}\n"
                              f"       cur: {b!r}")
                        break
                if len(g) != len(c):
                    print(f"     line count gen={len(g)} cur={len(c)}")
        for fam in mf.get("spatialrel_families", []):
            if not fam.get("reference"):
                continue
            p = ROOT / fam["file"]
            gen = render_spatialrels(fam)
            cur = extract_spatialrels(p.read_text(), fam["family"]) if p.exists() else ""
            same = gen == cur
            ok = ok and same
            print(f"[{'OK ' if same else 'DIFF'}] self-regen spatialrels {fam['family']} "
                  f"-> {pathlib.Path(fam['file'])}")
            if not same:
                g, c = gen.splitlines(), cur.splitlines()
                for n, (a, b) in enumerate(zip(g, c), 1):
                    if a != b:
                        print(f"     first diff line {n}:\n       gen: {a!r}\n"
                              f"       cur: {b!r}")
                        break
                if len(g) != len(c):
                    print(f"     line count gen={len(g)} cur={len(c)}")
        return 0 if ok else 1

    for sub in subs:
        if sub.get("reference"):
            continue
        for beh in sub["files"]:
            p = target_path(beh, sub, positions)
            if args.check:
                print(f"would write {p.relative_to(ROOT)}")
                continue
            p.parent.mkdir(parents=True, exist_ok=True)
            p.write_text(render(beh, sub))
            print(f"wrote {p.relative_to(ROOT)}")

    for bt in mf.get("boxtypes", []):
        if bt.get("reference"):
            continue
        p = ROOT / bt["file"]
        if args.check:
            print(f"would splice boxops {bt['box']} -> {bt['file']}")
            continue
        p.write_text(splice_boxops(p.read_text(), bt["box"], render_boxops(bt)))
        print(f"spliced boxops {bt['box']} -> {bt['file']}")

    for fam in mf.get("spatialrel_families", []):
        if fam.get("reference"):
            continue
        p = ROOT / fam["file"]
        if args.check:
            print(f"would splice spatialrels {fam['family']} -> {fam['file']}")
            continue
        p.write_text(splice_spatialrels(p.read_text(), fam["family"],
                                        render_spatialrels(fam)))
        print(f"spliced spatialrels {fam['family']} -> {fam['file']}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
