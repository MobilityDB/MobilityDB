"""Generate the SQL for the set-set spatial-join functions.

The set-set spatial joins generalise the scalar ever/always/temporal spatial
relationships to two arrays of temporal geos, returning the qualifying (i, j)
index pairs (and, for the temporal predicates, the periods during which each
pair holds).  Every SQL function is a thin wrapper over one of the twelve
type-generic C functions; the surface is therefore fully derivable from the
predicate x type matrix below, which mirrors the type coverage of the scalar
spatial relationships:

  - the within-distance, intersects, and disjoint predicates are defined for
    temporal points (tgeompoint, tgeogpoint) and temporal geometries
    (tgeometry, tgeography);
  - the touches predicate is defined for temporal geometries only (the GEOS
    DE-9IM relate matrix is planar-only, so geodetic touches is undefined);
  - the temporal within-distance predicate is defined for the planar types
    only (tgeompoint, tgeometry).

Run this script from the repository root to regenerate the two output files in
mobilitydb/sql/geo/.  Do not edit the generated files by hand.
"""

